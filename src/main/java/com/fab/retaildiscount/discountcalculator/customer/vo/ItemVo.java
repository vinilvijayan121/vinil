package com.fab.retaildiscount.discountcalculator.customer.vo;

public class ItemVo {
	private double dSellingPrice;
	private String sName;
	private String skuType;
	private int quantity;
	
	public double getdSellingPrice() {
		return dSellingPrice;
	}
	public void setdSellingPrice(double dSellingPrice) {
		this.dSellingPrice = dSellingPrice;
	}
	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public String getSkuType() {
		return skuType;
	}
	public void setSkuType(String skuType) {
		this.skuType = skuType;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	

}
